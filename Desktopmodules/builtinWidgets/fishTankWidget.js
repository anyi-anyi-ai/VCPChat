    /**
     * 生成鱼箱挂件
     */
    async function spawnFishTankWidget() {
        var widgetId = 'builtin-fishtank';

        if (state.widgets.has(widgetId)) return;

        var widgetData = widget.create(widgetId, {
            x: 700,
            y: CONSTANTS.TITLE_BAR_HEIGHT + 20,
            width: 340,
            height: 400,
        });

        widgetData.contentBuffer = FISH_TANK_HTML;
        widgetData.contentContainer.innerHTML = FISH_TANK_HTML;
        widget.processInlineStyles(widgetData);
        widgetData.isConstructing = false;
        widgetData.element.classList.remove('constructing');
        widget.autoResize(widgetData);

        setTimeout(function () {
            widget.processInlineScripts(widgetData);
        }, 100);

        console.log('[VCPdesktop] Fish tank widget spawned. 🐟');
    }

    // ============================================================
    // 导出
    // ============================================================
    window.VCPDesktop = window.VCPDesktop || {};
    window.VCPDesktop.builtinFishTank = {
        spawn: spawnFishTankWidget,
    };

