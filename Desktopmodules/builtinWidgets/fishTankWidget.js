        '',
        '            // 速度限制',
        '            var speed = Math.sqrt(fish.vx * fish.vx + fish.vy * fish.vy);',
        '            var maxSpeed = nearestFood ? 2.0 : 1.2;',
        '            if (speed > maxSpeed) {',
        '                fish.vx = (fish.vx / speed) * maxSpeed;',
